  // Função para limpar códigos RTF
  cleanRTFText(text) {
    if (!text || typeof text !== 'string') return text;
    
    return text
      .replace(/\\par\b/g, '')
      .replace(/\\[a-zA-Z]+\d*/g, '')
      .replace(/\{\\[^}]*\}/g, '')
      .replace(/\{\}/g, '')
      .replace(/\\\\/g, '')
      .replace(/\s+/g, ' ')
      .trim();
  }

