  // Função auxiliar para carregar capítulos do mapa
  loadChaptersFromMap(bookKey) {
    const chapters = this.chaptersMap[bookKey];
    if (!chapters) return [];
    
    return chapters.map(chapter => {
      if (chapter && chapter.verses) {
        const cleanedVerses = chapter.verses.map(verse => ({
          ...verse,
          text: this.cleanRTFText(verse.text)
        }));
        return { ...chapter, verses: cleanedVerses };
      }
      return chapter;
    });
  }

